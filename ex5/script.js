let opcaoUsuario = prompt("Escolha uma opção (0, 1, 2, 3, 4, 5, 6, 7, 8 ou 9):");

switch (opcaoUsuario) {
    case "0":
        alert("Você escolheu a opção zero.");
        break;
    case "1":
        alert("Você escolheu a opção um.");
        break;
    case "2":   
        alert("Você escolheu a opção dois.");
        break;
    case "3":
        alert("Você escolheu a opção três.");
        break;
    case "4":
        alert("Você escolheu a opção quatro.");
        break;
    case "5":
        alert("Você escolheu a opção cinco.");
        break;
    case "6":
        alert("Você escolheu a opção seis.");
        break;
    case "7":
        alert("Você escolheu a opção sete.");
        break;
    case "8":
        alert("Você escolheu a opção oito.");
        break;
    case "9":
        alert("Você escolheu a opção nove.");
        break;
    default:
        alert("Opção inválida. Por favor, escolha um número entre 0 e 9.");
}